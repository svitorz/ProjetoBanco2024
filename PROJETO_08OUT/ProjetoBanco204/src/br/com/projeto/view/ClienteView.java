    public void listar(){
        ClienteDAO clienteDao = new ClienteDAO();
        List<ClienteModel> lista = clienteDao.listarClientes();
        DefaultTableModel tabela = (DefaultTableModel) tabCliente.getModel();
        tabela.setNumRows(0);
        
        for(ClienteModel cm: lista){
             tabela.addRow(new Object[]{
                cm.getCodcli(),
                cm.getNome(),
                cm.getCpf(),
                cm.getRua(),
                cm.getNumero(),
                cm.getEstado(),
                cm.getBairro(),
                cm.getTelefone(),
                cm.getGenero()
             });
        }
    }

