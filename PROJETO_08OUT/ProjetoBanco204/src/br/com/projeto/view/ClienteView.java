    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        //CRIAR A ESTRUTUR TRY CATCH
        try{
            //criar um objeto do tipo Model
            ClienteModel objClienteModel=
                    new ClienteModel();
            
            //para atributo, vamos definir os valores
            //a serem atribuídos
            objClienteModel.setNome(txtNome.getText());
            objClienteModel.setBairro(txtBairro.getText());
            objClienteModel.setCpf(txtfCPF.getText());
            objClienteModel.setEstado(
                    cbEstado.getSelectedItem().toString());;
            objClienteModel.setNumero(
                    Integer.parseInt(txtNumero.getText()));
            objClienteModel.setRua(txtRua.getText());
            objClienteModel.setTelefone(txtfTelefone.getText());
            objClienteModel.setGenero(
                    cbGenero.getSelectedItem().toString());
            
            //vamos criar uma instância do ClienteDAO
            //para ter acesso ao método cadastrar no
            //banco de dados
            ClienteDAO objClienteDAO=
                    new ClienteDAO();
            
            //vamos chamar o método
            objClienteDAO.cadastrarCliente(objClienteModel);
            
            //se tudo der certo e passar pela linha anterior,
            //podemos mostrar que os dados foram inseridos:
            JOptionPane.showMessageDialog(null,
                    "Dados cadastrados com sucesso!");
            
            
            
            
            
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,
                "Não cadastrado? "+e.getMessage());
        }
    }//GEN-LAST:event_btnInserirActionPerformed

